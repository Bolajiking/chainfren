import { createHash } from 'node:crypto'
import { readdir, readFile, stat, writeFile } from 'node:fs/promises'
import { join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = fileURLToPath(new URL('..', import.meta.url))
const thesis = join(root, 'content/chainfren-thesis')
const output = join(thesis, 'generated-content-hash.mjs')
const canonicalRoots = [
  join(thesis, 'chapters'), join(thesis, 'manifest.mjs'), join(thesis, 'claims.mjs'),
  join(thesis, 'citations.mjs'), join(thesis, 'public-config.mjs'), join(thesis, 'public-system.mjs'),
  join(thesis, 'map-layout.mjs'), join(thesis, 'short-read.mdx'),
]

async function files(path) {
  const entries = await readdir(path, { withFileTypes: true })
  return (await Promise.all(entries.map((entry) => entry.isDirectory() ? files(join(path, entry.name)) : [join(path, entry.name)]))).flat()
}

const paths = (await Promise.all(canonicalRoots.map(async (path) => (await stat(path)).isDirectory() ? files(path) : [path]))).flat()
  .sort((left, right) => relative(root, left).split('\\').join('/').localeCompare(relative(root, right).split('\\').join('/')))
const hash = createHash('sha256')
for (const path of paths) {
  const name = relative(root, path).split('\\').join('/')
  const content = (await readFile(path, 'utf8')).replace(/\r\n?/g, '\n')
  hash.update(`${name}\0${content}\0`)
}
const value = hash.digest('hex')
await writeFile(output, `// Generated by scripts/hash-thesis-content.mjs. Do not edit.\nexport const THESIS_CONTENT_HASH = '${value}'\n`)
console.log(value)
